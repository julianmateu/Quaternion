public class ZeroNormException extends Exception {

    public ZeroNormException() {
    }

    public ZeroNormException(String message) {
        super(message);
    }

    public ZeroNormException(Throwable cause) {
        super(cause);
    }

    public ZeroNormException(String message, Throwable cause) {
        super(message, cause);
    }
}
